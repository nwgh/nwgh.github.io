function updatePossibilities() {
    let pattern = new RegExp(document.querySelector("#pattern").value, 'i');
    let not_contains = new RegExp("[" + document.querySelector("#notcontains").value + "]", 'i');
    let possibilities = words.
        filter((w) => pattern.test(w)).
        filter((w) => !not_contains.test(w));

    let contains = Array.from(document.querySelector("#contains").value);
    contains.forEach((c) => possibilities = possibilities.filter((w) => new RegExp(c, 'i').test(w)));

    let output = document.querySelector("#possibilities");
    output.value = "";
    possibilities.forEach((w) => output.value = output.value + w + "\n");
}
